void solve() {
    lli n, a, p;
    cin >> n >> p;
    lli ans = -1;

    for (lli i = 1; i <= 40; i++) {
        lli val = n - i * p, cnt = 0;
        if (val < 0) {
            continue;
        }
        while (val > 0) {
            cnt += val % 2;
            val /= 2;
        }

        if (cnt == i) {
            ans = i;
            break;
        } else if (cnt < i) {
            val = n - i * p;
            if (val >= i) {
                ans = i;
                break;
            }
        }
    }

    cout << ans << endl;
}

